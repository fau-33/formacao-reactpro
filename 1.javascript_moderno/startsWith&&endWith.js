// startsWith - Verifica se uma string começa com um determinado valor
const texto = "Ser ou não ser, eis a questão";

const resultado = texto.startsWith("não ser", 7);

console.log(resultado);

// endsWith - Verifica se uma string termina com um determinado valor
const texto2 = "Ser ou não ser, eis a questão";

const resultado2 = texto2.endsWith("questão");

console.log(resultado2);
